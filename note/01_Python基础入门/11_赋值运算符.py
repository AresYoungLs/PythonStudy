# 赋值运算符
# 	=      可以将等号右侧的值赋值给左侧的变量
# 	+=     a += 5相当于 a = a + 5
# 	-=     a -= 5相当于 a = a - 5
# 	*=     a *= 5相当于 a = a * 5
# 	**=    a **= 5相当于 a = a ** 5
# 	/=     a /= 5相当于 a = a / 5
# 	//=    a //= 5相当于 a = a // 5
# 	%=     a %= 5相当于 a = a % 5
a = 10
a = 20

a += 5
a -= 5
a *= 5
a **= 5
a /= 5
a //= 5  # 在对浮点数进行算数运算的时候，也会返回一个浮点数
a %= 4