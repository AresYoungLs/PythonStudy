
# 算数运算符
#    +     加法运算符（如果是两个字符串之间进行加法，则会进行拼串操作）
#    -     减法运算符（不支持字符串操作）
#    *     乘法运算符（如果将字符串和数字相乘，则会进行复制操作，将字符串重复指定次数）
#    /     除法运算符（运算时结果总会返回一个浮点类型，并且除数不能为0）
#    //    整除，只会保留计算后的整数位
#    **    幂运算，求一个值的几次幂
#    %     取模（求两个数相除的余数）
a = 10 +5# 计算
print(a)
a = 'hello ' + ' world' # 拼串
print(a)
a = 5 -True 
